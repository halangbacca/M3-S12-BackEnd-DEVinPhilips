CREATE OR REPLACE VIEW V_ESTATISTICA AS
SELECT 1 ID, 'PACIENTE' TIPO, GENERO GRUPO, COUNT(1) VALOR fROM PACIENTE
WHERE GENERO = 'MASCULINO'
GROUP BY GENERO
UNION ALL
SELECT 2 ID, 'PACIENTE' TIPO, GENERO GRUPO, COUNT(1) VALOR fROM PACIENTE
WHERE GENERO = 'FEMININO'
GROUP BY GENERO
UNION ALL
SELECT 3 ID, 'PACIENTE' TIPO, 'QTD' GRUPO, COUNT(1) VALOR fROM PACIENTE
UNION ALL
SELECT 4 ID, 'EXAME' TIPO, 'QTD' GRUPO, COUNT(1) VALOR fROM EXAME
UNION ALL
SELECT 5 ID,'DIETA' TIPO, 'QTD' GRUPO, COUNT(1) VALOR fROM DIETA
UNION ALL
SELECT 6 ID,'EXERCICIO' TIPO, 'QTD' GRUPO, COUNT(1) VALOR fROM EXERCICIO
UNION ALL
SELECT 7 ID, 'MEDICAMENTO' TIPO, 'QTD' GRUPO, COUNT(1) VALOR fROM MEDICAMENTO;
