CREATE TABLE USUARIO
(
    ID       NUMBER GENERATED by default on null as IDENTITY PRIMARY KEY,
    NOME     VARCHAR2(200),
    GENERO   VARCHAR2(100),
    CPF      VARCHAR2(20),
    TELEFONE VARCHAR2(20),
    EMAIL    VARCHAR2(200),
    SENHA    VARCHAR2(200),
    NIVEL    VARCHAR2(100),
    SITUACAO NUMBER
)

